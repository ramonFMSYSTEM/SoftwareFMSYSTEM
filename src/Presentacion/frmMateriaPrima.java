    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if (txtCantidad.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese una cantidad");
            txtCantidad.requestFocus();
            return;
        }

        if (txtproducto.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese nombre del producto");
            txtproducto.requestFocus();
            return;
        }

        if (txtDes.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese una descripcion");
            txtDes.requestFocus();
            return;
        }

        if (txtPrecio.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Ingrese el precio");
            txtPrecio.requestFocus();
            return;
        }

        DmateriaPrima dts = new DmateriaPrima();
        LMateriaPrima func = new LMateriaPrima();

        int seleccionado = cboCategoria.getSelectedIndex();
        dts.setCategoria((String) cboCategoria.getItemAt(seleccionado));

        seleccionado = cboProve.getSelectedIndex();
        dts.setProveedor((String) cboProve.getItemAt(seleccionado));

        dts.setCantidad(Integer.parseInt(txtCantidad.getText()));

        seleccionado = cboUnidad.getSelectedIndex();
        dts.setUnidad_medidad((String) cboUnidad.getItemAt(seleccionado));

        dts.setProducto(txtproducto.getText());

        dts.setDescripcion(txtDes.getText());

        dts.setPrecio(Double.parseDouble(txtPrecio.getText()));

        dts.setFecha_compra(txtFecha_compra.getText());

        dts.setFecha_cad(txtFecha_cad.getText());

        if (accion.equals("guardar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Producto registrado satisfactoriamente!");
                mostrar("");
                inhabilitar();
            }
        } else if (accion.equals("editar")) {
            dts.setID(Integer.parseInt(txtID.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Producto actualizado");
                mostrar("");
                inhabilitar();
            }
        }


    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (!txtID.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro de eliminar el registro?", "Acpetar", 2);

            if (confirmacion == 0) {
                LMateriaPrima func = new LMateriaPrima();
                DmateriaPrima dts = new DmateriaPrima();

                dts.setID(Integer.parseInt(txtID.getText()));
                func.eliminar(dts);
                mostrar("");
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

