    private void tablaProdMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaProdMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("editar");
        habilitar();
        btnEliminar.setEnabled(true);
        accion = "editar";

        int fila = tablaProd.rowAtPoint(evt.getPoint());

        cboCategoria.setSelectedItem(tablaProd.getValueAt(fila, 0).toString());

        cboProve.setSelectedItem(tablaProd.getValueAt(fila, 1).toString());

        txtID.setText(tablaProd.getValueAt(fila, 2).toString());

        txtCantidad.setText(tablaProd.getValueAt(fila, 3).toString());

        cboUnidad.setSelectedItem(tablaProd.getValueAt(fila, 4).toString());

        txtproducto.setText(tablaProd.getValueAt(fila, 5).toString());

        txtDes.setText(tablaProd.getValueAt(fila, 6).toString());

        txtPrecio.setText(tablaProd.getValueAt(fila, 7).toString());

        txtFecha_compra.setText(tablaProd.getValueAt(fila, 8).toString());

        txtFecha_cad.setText(tablaProd.getValueAt(fila, 9).toString());


    }//GEN-LAST:event_tablaProdMouseClicked

